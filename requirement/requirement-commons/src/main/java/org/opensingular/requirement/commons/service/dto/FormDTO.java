package org.opensingular.requirement.commons.service.dto;

import java.io.Serializable;

public class FormDTO implements Serializable {

    private String name;

    private String abbreviation;

    private String description;

    private boolean newable;

    public FormDTO() {
    }

    public FormDTO(String name, String abbreviation, String description, boolean newable) {
        this.name = name;
        this.abbreviation = abbreviation;
        this.description = description;
        this.newable = newable;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getAbbreviation() {
        return abbreviation;
    }

    public void setAbbreviation(String abbreviation) {
        this.abbreviation = abbreviation;
    }

    public boolean isNewable() {
        return newable;
    }

    public void setNewable(boolean newable) {
        this.newable = newable;
    }
}
